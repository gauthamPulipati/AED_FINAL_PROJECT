    public void populateCourseCatalogTable() {

        DefaultTableModel model = (DefaultTableModel) tableCourseOfferings.getModel();
        model.setRowCount(0);
        CourseOffer co = new CourseOffer();

        for (Course c : courseCatalog.getCourseList()) {
            Object row[] = new Object[5];
            row[0] = c.getName();
            row[1] = c.getCOurseNumber();
            row[2] = c.getCredits();
//          row[3] =     // Professor Name
            row[4] = co.getSeatsAvailable();
            model.addRow(row);
        }

    }

