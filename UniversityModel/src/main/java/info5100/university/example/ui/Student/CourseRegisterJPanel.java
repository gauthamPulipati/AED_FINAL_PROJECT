    private void btnAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCourseActionPerformed
        int selectedRowIndex = tableCourseOfferings.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Course first!!!");
        }

        int i = 0;
        //   Course course = (Course) tableCourseOfferings.getValueAt(selectedRowIndex, 0);
        Object courseNo = tableCourseOfferings.getValueAt(selectedRowIndex, 0);
        Object courseName = tableCourseOfferings.getValueAt(selectedRowIndex, 1);
        Object Credit = tableCourseOfferings.getValueAt(selectedRowIndex, 2);

        ArrayList<Object> cartDetails = new ArrayList<Object>();
        cartDetails.add(courseNo);
        cartDetails.add(courseName);
        cartDetails.add(Credit);

        populateCartTable(cartDetails);
        
        sum = sum + Integer.parseInt(String.valueOf(tableCourseOfferings.getValueAt(0, 2)));
        
        txtCreditTot.setText(String.valueOf(sum));
       
    }//GEN-LAST:event_btnAddCourseActionPerformed

