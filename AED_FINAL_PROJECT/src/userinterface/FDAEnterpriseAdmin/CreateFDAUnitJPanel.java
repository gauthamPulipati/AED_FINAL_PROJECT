    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblFDA.getModel();
        model.setRowCount(0);
        
        for(FDA fda:enterprise.getFdas().getFDA()){
            Object[] row = new Object[1];
            row[0] = fda;
            model.addRow(row);
        }
    }

